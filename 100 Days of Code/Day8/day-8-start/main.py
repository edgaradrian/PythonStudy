def greet_with_name(name):
  print(f"Welcome {name}")
  print(f"Hi {name}")
  print("[xxxxxxxxxxxxxx]")

greet_with_name("Edgar Adrián")

def greet_with(name, location):
  print(f"Welcome {name}")
  print(f"What is in like in {location}")
  print("[xxxxxxxxxxxxxx]")

greet_with("Edgar Adrián", "Distrito Federal")

